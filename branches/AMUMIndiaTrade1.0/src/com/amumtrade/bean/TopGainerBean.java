package com.amumtrade.bean;

public class TopGainerBean {
	private String companyName ;
	private String high ;
	private String low ;
	private String lastPrice ;
	private String prvClose ;
	private String change ;
	private String percentGain ;
	private String api;
	
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public String getHigh() {
		return high;
	}
	public void setHigh(String high) {
		this.high = high;
	}
	public String getLow() {
		return low;
	}
	public void setLow(String low) {
		this.low = low;
	}
	public String getLastPrice() {
		return lastPrice;
	}
	public void setLastPrice(String lastPrice) {
		this.lastPrice = lastPrice;
	}
	public String getPrvClose() {
		return prvClose;
	}
	public void setPrvClose(String prvClose) {
		this.prvClose = prvClose;
	}
	public String getChange() {
		return change;
	}
	public void setChange(String change) {
		this.change = change;
	}
	public String getPercentGain() {
		return percentGain;
	}
	public void setPercentGain(String percentGain) {
		this.percentGain = percentGain;
	}
	public String getApi() {
		return api;
	}
	public void setApi(String api) {
		this.api = api;
	}
	
	
}
