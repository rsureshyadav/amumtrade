package com.amumtrade.bean;

public class StockWebBean {
	public String symbol;
	public String name;
	public String lastSale;
	public String marketCap;
	public String adrTso;
	public String IPOyear;
	public String sector;
	public String industry;
	public String summaryQuote;

	public String getSymbol() {
		return symbol;
	}
	public void setSymbol(String symbol) {
		this.symbol = symbol;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLastSale() {
		return lastSale;
	}
	public void setLastSale(String lastSale) {
		this.lastSale = lastSale;
	}
	public String getMarketCap() {
		return marketCap;
	}
	public void setMarketCap(String marketCap) {
		this.marketCap = marketCap;
	}
	public String getAdrTso() {
		return adrTso;
	}
	public void setAdrTso(String adrTso) {
		this.adrTso = adrTso;
	}
	public String getIPOyear() {
		return IPOyear;
	}
	public void setIPOyear(String oyear) {
		IPOyear = oyear;
	}
	public String getSector() {
		return sector;
	}
	public void setSector(String sector) {
		this.sector = sector;
	}
	public String getIndustry() {
		return industry;
	}
	public void setIndustry(String industry) {
		this.industry = industry;
	}
	public String getSummaryQuote() {
		return summaryQuote;
	}
	public void setSummaryQuote(String summaryQuote) {
		this.summaryQuote = summaryQuote;
	}
	
	

}
